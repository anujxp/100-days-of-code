// JavaCompilationWorkflow.java
public class JavaCompilationWorkflow {
    public static void main(String[] args) {
        System.out.println("How Java Code Runs:");
        System.out.println("1. Write Java Source Code → file.java");
        System.out.println("2. Compile with javac → file.class (Bytecode)");
        System.out.println("3. JVM interprets Bytecode → Machine Code");
        System.out.println("4. Output is displayed.");
        System.out.println("This ensures platform independence (WORA).");
    }
}
